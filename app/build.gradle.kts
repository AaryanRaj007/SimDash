tasks.register("deleteWebpIcons") {
    doLast {
        val mipmapDirs = file("src/main/res").listFiles { f -> f.isDirectory && f.name.startsWith("mipmap") }
        mipmapDirs?.forEach { dir ->
            dir.listFiles { f -> f.name.endsWith(".webp") }?.forEach { it.delete() }
        }
    }
}

tasks.whenTaskAdded {
    if (name == "preBuild") {
        dependsOn("deleteWebpIcons")
    }
}

